# %%

import math

# %%

# greatest common denominator
print(math.gcd(64, 34))

# %%

# sum of iterables
print(math.fsum(range(1, 30)))

# %%

# squaroot
print(math.sqrt(2))

# %%

# angle conversions
print(math.degrees(math.pi))
print(math.radians(180))

# %%

# trig functions
print(math.sin(math.pi / 2))
print(math.cos(0))
print(math.tan(math.pi / 4))

# arc and hyperbolic also available

# %%

# length of hypotenuse
print(math.hypot(3, 4))

# %%

# from python 3.8

# combinatorics
print(math.comb(6, 2))
print(math.perm(6, 2))

# integer sqrt
print(math.isqrt(2))

# product
print(math.prod(range(1, 10)))
