# can define functions within functions
def parent():
    print("Printing from the parent() function")

    def first_child():
        print("Printing from the first_child() function")

    def second_child():
        print("Printing from the second_child() function")

    second_child()
    first_child()


# inner function order depends on order called
parent()
# cannot call inner functions without calling parent function
